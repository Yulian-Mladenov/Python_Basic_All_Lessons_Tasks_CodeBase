product = input()

if product == "banana" or product == "apple" or product == "kiwi":
    print("fruit")
elif product == "cherry" or product == "lemon" or product == "grapes":
    print("fruit")
elif product == "cucumber" or product == "pepper" or product == "carrot" or product == "tomato":
    print("vegetable")
else:
    print("unknown")